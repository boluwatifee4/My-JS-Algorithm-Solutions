// =========> Question <==========
// Given a string return a new string with the reversed order of the original string.
// Example:
// reverseString('hello') === 'olleh'
// reverseString('Howdy') === 'ydwoH'

const reverseString = (str) => {
    return console.log(str.split("").reverse().join(""))
}

reverseString("hello");